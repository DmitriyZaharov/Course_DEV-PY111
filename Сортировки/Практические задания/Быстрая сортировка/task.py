from typing import List


def sort(container: List[int]) -> List[int]:
    """
    Алгоритм быстрой сортировки.

    Сложность по времени: В среднем O(n \log n).
    Однако для уже отсортированного массива выбор первого элемента в
    качестве опорного приведет к худшему случаю — O(n^2).
    Сложность по памяти: Модификация создает новые списки на каждом шаге рекурсии,
    поэтому требует дополнительной памяти.

    Алгоритм работы:
    1. Выбираем опорный элемент. Например, первый элемент.
    2. В левую часть отправляем всё что меньше опорного элемента, в правую всё что больше.
    3. К левой и правой части рекурсивно применяет алгоритм быстрой сортировки.

    :param container: последовательность, которую надо отсортировать
    :return: Отсортированная в порядке возрастания последовательность
    """
    # Базовый случай рекурсии: пустой список или список из одного элемента уже отсортирован
    if len(container) <= 1:
        return container

    # 1. Выбираем опорный элемент (первый элемент списка)
    pivot = container[0]

    # 2. Распределяем остальные элементы по спискам
    # Элементы, которые меньше или равны опорному (исключая сам pivot с индекса 0)
    left = [x for x in container[1:] if x <= pivot]

    # Элементы, которые строго больше опорного
    right = [x for x in container[1:] if x > pivot]

    # 3. Рекурсивно сортируем левую и правую части и объединяем результат
    return sort(left) + [pivot] + sort(right)

