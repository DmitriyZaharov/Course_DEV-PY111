from typing import List


def merge(left: List[int], right: List[int]) -> List[int]:
    """
    Процедура слияния двух отсортированных массивов в один.
    """
    result = []
    i = j = 0

    # Сравниваем элементы из обоих массивов и добавляем меньший в результат
    while i < len(left) and j < len(right):
        if left[i] <= right[j]:
            result.append(left[i])
            i += 1
        else:
            result.append(right[j])
            j += 1

    # Добавляем оставшиеся элементы, если один из массивов закончился раньше
    result.extend(left[i:])
    result.extend(right[j:])

    return result


def sort(container: List[int]) -> List[int]:
    """
    Алгоритм сортировки слиянием.

    1. Если массив состоит из 1 элемента – он отсортирован
    2. Иначе массив разбивается на две части, которые сортируются рекурсивно
    3. После сортировки двух частей массива к ним применяется процедура слияния

    Стабильная сложность по времени: всегда O(nlog n).
    В отличие от быстрой сортировки (Quick Sort), сортировка слиянием гарантирует эту скорость
    как в лучшем, так и в худшем (например, на уже отсортированном или обратно отсортированном
    массиве) случае.
    Сложность по памяти: O(n).

    Алгоритму требуется дополнительное пространство для создания временных подмассивов при
    разделении и слиянии.
    Устойчивость (Stability): Алгоритм является устойчивым.
    Благодаря условию left[i] <= right[j] в функции merge, одинаковые элементы сохраняют свой
    исходный взаимный порядок.

    :param container: Массив, который надо отсортировать
    :return: Отсортированный в порядке возрастания массив
    """
    # 1. Базовый случай: массив из 1 элемента (или пустой) уже отсортирован
    if len(container) <= 1:
        return container

    # 2. Разбиваем массив на две части по центру
    mid = len(container) // 2

    # Рекурсивно сортируем левую и правую части
    left_sorted = sort(container[:mid])
    right_sorted = sort(container[mid:])

    # 3. Применяем процедуру слияния к отсортированным частям
    return merge(left_sorted, right_sorted)

