from typing import List


def majority_element(nums: List[int]) -> int:
    """
    Находит преобладающий элемент в массиве (который встречается более n // 2 раз).
    Использует алгоритм большинства голосов Бойера — Мура.

    По времени: O(n) — массив просматривается ровно один раз.
    По памяти: O(1)— выделяется память только под две переменные (candidate и count),
    независимо от размера списка.

    :param nums: Список целых чисел
    :return: Преобладающий элемент
    """
    candidate = None
    count = 0

    for num in nums:
        # Если счетчик равен нулю, выбираем текущее число в качестве нового кандидата
        if count == 0:
            candidate = num
            count = 1
        # Если текущее число совпадает с кандидатом, увеличиваем счетчик
        elif num == candidate:
            count += 1
        # Если не совпадает, уменьшаем счетчик
        else:
            count -= 1

    # По условию задачи преобладающий элемент всегда существует,
    # поэтому candidate гарантированно является правильным ответом.
    return candidate
