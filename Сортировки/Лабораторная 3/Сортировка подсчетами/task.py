from typing import Sequence


def sort(container: Sequence[int]) -> Sequence[int]:
    """
    Сортировка подсчетами

    Временная сложность: O(N + K), где N — количество элементов в массиве,
    а K — максимальное значение (max_val). Это делает алгоритм линейным, если K не превышает
    N значительно. Пространственная сложность: O(K) для хранения вспомогательного массива подсчетов.

    1. Определите максимальное значение в массиве и заполните вспомогательный массив с подсчетом количества элементов.
    2. Посчитайте количество каждого объекта
    3. Зная количество каждого объекта, восстановите отсортированный массив

    :param container: Массив, который надо отсортировать
    :return: Отсортированный в порядке возрастания массив
    """
    # Если контейнер пустой, возвращаем пустой список
    if not container:
        return []

    # 1. Нахождение максимального значения для определения размера вспомогательного массива
    max_val = max(container)

    # Создание массива счетчиков, заполненного нулями (размер max_val + 1, так как мин. элемент = 0)
    counts = [0] * (max_val + 1)

    # 2. Подсчет количества каждого объекта
    for item in container:
        counts[item] += 1

    # 3. Восстановление отсортированного массива по данным из counts
    sorted_result = []
    for value, count in enumerate(counts):
        if count > 0:
            sorted_result.extend([value] * count)

    return sorted_result
