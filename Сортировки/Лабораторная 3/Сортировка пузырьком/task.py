from typing import Sequence


def sort(container: Sequence[int]) -> Sequence[int]:
    """
    Сортировка пузырьком

    1. Пройти по всему массиву, сравнивая каждые два соседних элемента.
    2. Если элементы не находятся в нужном порядке, меняем их местами.
    3. Повторяйте шаг 2, пока не пройдем весь массив без изменений.
    4. Повторяем шаги 1-3, пока не отсортируете весь массив.

     Сложность алгоритма:
     В худшем и среднем случае: O(n²)
     (когда массив отсортирован в обратном порядке или сильно перемешан).
     В лучшем случае: O(n) (благодаря проверке флага swapped).
     Память: O(n) для создания копии исходного контейнера.

    :param container: Массив, который надо отсортировать
    :return: Отсортированный в порядке возрастания массив
    """
    # Создаем изменяемую копию, чтобы не менять исходный sequence
    arr = list(container)
    n = len(arr)

    # Внешний цикл отвечает за количество проходов
    for i in range(n):
        # Оптимизация 1: флаг для отслеживания перестановок на текущем шаге
        swapped = False

        # Внутренний цикл для сравнения соседних элементов
        # Оптимизация 2: уменьшаем диапазон на 'i', так как 'i' элементов уже на своих местах
        for j in range(0, n - i - 1):
            if arr[j] > arr[j + 1]:
                # Меняем элементы местами
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                swapped = True

        # Если перестановок не было, массив уже отсортирован
        if not swapped:
            break

    return arr
