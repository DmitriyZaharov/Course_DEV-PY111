"""
    6. Аренда ракет
    Вы – компания, дающая в аренду ракеты. Каждый день к вам приходит список заявок на использование ракет в виде: 
    (час_начала, час_конца), (час_начала, час_конца), ...
    Если аренда ракеты заканчивается в час X, то в этот же час ее уже можно взять в аренду снова 
    (т.е. час_начала может начинаться с Х).
    Дано: список заявок на использование ракет
    Задача: вывести ответ, хватит ли вам одной ракеты, чтобы удовлетворить все заявки на этот день
    
    Условие непересечения:
    Две заявки (start1, end1) и (start2, end2) не пересекаются, если:
    end1 <= start2 (первая заканчивается до или в момент начала второй)
    ИЛИ 
    end2 <= start1 (вторая заканчивается до или в момент начала первой)
"""


def can_one_rocket_suffice(requests: List[Tuple[int, int]]) -> bool:
    """
    Проверяет, достаточно ли одной ракеты для всех заявок.

    Args:
        requests: Список заявок в формате (час_начала, час_конца)

    Returns:
        True, если одной ракеты достаточно, иначе False

    Временная сложность: O(n log n) - из-за сортировки
    Пространственная сложность: O(1) - если сортировать in-place
    """
    if len(requests) <= 1:
        return True

    # Сортируем по времени начала
    sorted_req = sorted(requests)

    for i in range(len(sorted_req) - 1):
        start1, end1 = sorted_req[i]
        start2, end2 = sorted_req[i + 1]

        # Проверяем пересечение
        if not (end1 <= start2):  # Если первая не заканчивается до начала второй
            return False

    return True

"""
    7. Сорт
    Дано: массив из 10**6 целых чисел, каждое из которых лежит на отрезке [13, 25].
    Задача: отсортировать массив наиболее эффективным способом
    
    Наиболее эффективным способом для этой задачи является сортировка подсчетом.
    Она работает за линейное время O(n) и требует минимум памяти:
    - узкий диапазон: 13 значений
"""


def counting_sort_range(arr: List[int], min_val: int = 13, max_val: int = 25) -> List[int]:
    """
    Сортировка подсчётом для массива целых чисел из известного диапазона.
    Временная сложность: O(n + k), где k = max_val - min_val + 1
    Пространственная сложность: O(k)
    """
    if not arr:
        return arr

    range_size = max_val - min_val + 1
    count = [0] * range_size

    # Считаем частоту каждого числа
    for num in arr:
        count[num - min_val] += 1

    # Быстрая сборка без создания временных промежуточных списков в памяти
    result = []
    for i in range(range_size):
        if count[i] > 0:
            val = min_val + i  # Определяем, какое число сейчас восстанавливаем
            for _ in range(count[i]):  # Повторяем цикл столько раз, сколько это число встретилось
                result.append(val)  # Добавляем его по одному в конец результирующего списка
    return result


