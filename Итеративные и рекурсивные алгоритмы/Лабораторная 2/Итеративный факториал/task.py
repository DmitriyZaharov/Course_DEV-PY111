def factorial_iterative(n: int) -> int:
    """
    Рассчитать факториал числа n итеративным способом

    Алгоритм последовательно умножает числа от 1 до n внутри цикла, что позволяет
    избежать переполнения стека вызовов, характерного для рекурсии.

    Сложность: Временная сложность алгоритма составляет O(n), так как цикл выполняет
    ровно n - 1 итераций умножения. Затраты памяти — O(1), поскольку используется лишь
    одна переменная для хранения результата.

    :param n: Число, факториал которого нужно найти
    :return: n! - факториал числа n
    """
    # Факториал определен только для неотрицательных чисел
    if n < 0:
        raise ValueError("Factorial is not defined for negative numbers")

    # Начальное значение произведения (базовый случай для 0! и 1!)
    result = 1

    # Последовательно умножаем результат на числа от 2 до n включительно
    for i in range(2, n + 1):
        result *= i

    return result
