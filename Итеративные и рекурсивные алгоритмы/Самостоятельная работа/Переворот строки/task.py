from typing import List


def reverse_string(s: List[str]) -> None:
    """
    Разворачивает список символов на месте (in-place).
    Ничего не возвращает, изменяет исходный список.

    По времени: O(n) — мы проходим ровно до половины списка,
    совершая n/2 обменов.
    По памяти: O(1) — дополнительная память не выделяется,
    так как все операции происходят внутри исходного массива s.
    """
    left, right = 0, len(s) - 1

    while left < right:
        # Меняем элементы местами без использования третьей переменной
        s[left], s[right] = s[right], s[left]
        left += 1
        right -= 1
