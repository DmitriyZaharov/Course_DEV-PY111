from typing import List


def car_paths(n: int, m: int) -> List[List[int]]:
    """
    Просчитать количество маршрутов до каждой клетки с учетом возможных перемещений.

    :param n: Количество строк в таблице
    :param m: Количество столбцов в таблице

    :return: Новую таблицу с посчитанным количеством маршрутов в каждую клетку
    Теперь движения три: вправо, вниз и по диагонали вправо-вниз.
    В каждую клетку (i, j) можно прийти из трех соседних:
    Сверху: (i - 1, j)
    Слева: (i, j - 1)
    По диагонали сверху-слева: (i - 1, j - 1)
    Соответственно, количество способов попасть в текущую клетку равно сумме способов достижения этих трех клеток.
    В стартовой клетке (0, 0) находится 1 автомобиль

    :param n: Количество строк в таблице
    :param m: Количество столбцов в таблице
    :return: Новую таблицу с посчитанным количеством маршрутов в каждую клетку

    """

    if n <= 0 or m <= 0:
        return []

    # Создаем таблицу размером n строк на m столбцов, заполненную нулями
    paths = [[0] * m for _ in range(n)]

    # Начальное условие: в стартовой клетке у нас 1 способ (стоять на старте)
    paths[0][0] = 1

    # Заполняем первую строку — сюда можно приехать только строго слева
    for j in range(1, m):
        paths[0][j] = paths[0][j - 1]

    # Заполняем первый столбец — сюда можно приехать только строго сверху
    for i in range(1, n):
        paths[i][0] = paths[i - 1][0]

    # Заполняем все остальные внутренние клетки
    for i in range(1, n):
        for j in range(1, m):
            # Суммируем пути: сверху + слева + по диагонали
            paths[i][j] = paths[i - 1][j] + paths[i][j - 1] + paths[i - 1][j - 1]

    return paths


if __name__ == '__main__':
    paths = car_paths(4, 2)
    print(paths[-1][-1])  # 7
