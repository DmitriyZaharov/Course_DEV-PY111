from typing import Union, Sequence


def stairway_path(stairway: Sequence[Union[float, int]]) -> Union[float, int]:
    """
    Рассчитайте минимальную стоимость подъема на верхнюю ступень,
    если мальчик умеет наступать на следующую ступень и перешагивать через одну.

    Память O(1): код не создает дополнительных массивов и хранит только две переменные
    для прошлых шагов.
    Скорость O(n): задача решается ровно за один проход по списку ступеней.

    :param stairway: список целых чисел, где каждое целое число является стоимостью конкретной ступени
    :return: минимальная стоимость подъема на верхнюю ступень
    """

    n = len(stairway)

    # Если ступеней нет, стоимость равна 0
    if n == 0:
        return 0
    # Если ступень одна, мальчик обязан на нее встать
    if n == 1:
        return stairway[0]

    # Начальные стоимости для первых двух ступеней
    # Мальчик может начать сразу с любой из них
    prev2 = stairway[0]
    prev1 = stairway[1]

    # Рассчитываем минимальную стоимость для каждой последующей ступени
    for i in range(2, n):
        current = min(prev1, prev2) + stairway[i]
        prev2 = prev1
        prev1 = current

    # Возвращаем стоимость для последней (верхней) ступени
    return prev1


if __name__ == '__main__':
    print(stairway_path([1, 3, 1, 5]))  # 7
