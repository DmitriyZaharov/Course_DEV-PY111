from typing import List


def rocket_coasts(table: List[List[int]]) -> List[List[int]]:
    """
    Просчитать минимальные стоимости маршрутов до каждой клетки с учетом возможных перемещений.

    :param table: Таблица размером N*M, где в каждой клетке дана стоимость перемещения в неё
    :return: Таблицу стоимостей перемещения по клеткам

    Так как в каждую клетку (i, j) можно попасть только сверху (i-1, j) или слева (i, j-1), то
    минимальная стоимость рассчитывается как цена текущей клетки плюс минимум из этих двух направлений.
    Особое внимание уделяем первой строке и первому столбцу — в них можно двигаться только в одном
    направлении (только вправо или только вниз соответственно).
    """
    ...  # TODO рассчитать таблицу стоимостей перемещений

    if not table or not table:
        return []

    rows = len(table)
    cols = len(table[0])

    # Создаем результирующую таблицу такого же размера
    # Копируем структуру, заполняя нулями
    coasts = [[0] * cols for _ in range(rows)]

    # Базовое значение: стартовая клетка (верхний левый угол)
    coasts[0][0] = table[0][0]

    # Заполняем первую строку — в неё можно прийти только слева
    for j in range(1, cols):
        coasts[0][j] = coasts[0][j - 1] + table[0][j]

    # Заполняем первый столбец — в него можно прийти только сверху
    for i in range(1, rows):
        coasts[i][0] = coasts[i - 1][0] + table[i][0]

    # Заполняем оставшиеся клетки
    for i in range(1, rows):
        for j in range(1, cols):
            # Цена текущей клетки + минимум из клетки сверху и клетки слева
            coasts[i][j] = table[i][j] + min(coasts[i - 1][j], coasts[i][j - 1])

    return coasts

