    ...  # TODO найти количество маршрутов до каждой ступени

    # Если ступеней меньше нуля, возвращаем пустой список
    if count_stairs < 0:
        raise ValueError("Количество ступеней должно быть не отрицательным числом")

    # Базовый случай для 0 ступеней
    if count_stairs == 0:
        return [0]

    # Создаем список нужной длины и задаем начальные условия
    # f(0) = 1, f(1) = 1
