from typing import Tuple
from functools import lru_cache


def calculate_paths(shape: Tuple[int, int]) -> int:
    """
    Дано поле с размером rows*cols посчитать количество ходов коня из верхнего левого угла в нижний правый

    :param shape: размер доски в виде кортежа
    :return: количество путей согласно заданным условиям

    Cтрелки относительно центральной черной точки (r, c), где r — строка, c — столбец
    (индексы увеличиваются вниз и вправо):
    Вверх-вправо: на 1 клетку вверх, 2 клетки вправо → смещение (-1, +2)
    Вправо-вниз: на 1 клетку вниз, 2 клетки вправо → смещение (+1, +2)
    Вниз-вправо: на 2 клетки вниз, 1 клетку вправо → смещение (+2, +1)
    Вниз-влево: на 2 клетки вниз, 1 клетку влево → смещение (+2, -1)
    Для ленивой динамики (Top-Down), которая идет из финиша обратно в старт,
    все эти векторы нужно обратить (поменять знаки на противоположные).
    То есть в текущую клетку (r, c) мы могли прийти из:
    (r + 1, c - 2)
    (r - 1, c - 2)
    (r - 2, c - 1)
    (r - 2, c + 1)
    """
    ... # TODO реализуйте подсчет ходов коня

    rows, cols = shape

    if rows <= 0 or cols <= 0:
        return 0

    @lru_cache(maxsize=None)
    def dp(r: int, c: int) -> int:
        # Базовый случай: успешно добрались до старта
        if r == 0 and c == 0:
            return 1

        # Выход за границы доски
        if not (0 <= r < rows and 0 <= c < cols):
            return 0

        # Суммируем пути из всех 4 возможных родительских клеток (обратные ходы)
        return (
            dp(r + 1, c - 2)
            + dp(r - 1, c - 2)
            + dp(r - 2, c - 1)
            + dp(r - 2, c + 1)
        )

    # Запускаем из правого нижнего угла (rows-1, cols-1)
    return dp(rows - 1, cols - 1)


if __name__ == '__main__':
    print(calculate_paths((4, 4)))  # 2
    print(calculate_paths((7, 15)))  # 13309
