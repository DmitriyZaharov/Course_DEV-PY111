"""
Так как робот может ходить только вправо или вниз, количество путей в любую клетку (i, j)
равно сумме путей, ведущих в неё сверху (i-1, j) и слева (i, j-1).

Временная сложность: O(m x n) — обходим сетку один раз.
Пространственная сложность: O(n) — храним только одну строку.
"""

def unique_paths(m: int, n: int) -> int:
    # Инициализируем строку единицами, так как в первую строчку
    # можно попасть только одним способом (идти всегда вправо)
    row = [1] * n

    # Проходим по остальным m-1 строкам
    for i in range(1, m):
        # Первую ячейку строки (индекс 0) не трогаем, туда всегда 1 путь (идти вниз)
        for j in range(1, n):
            # Новое значение = значение слева (row[j-1]) + значение сверху (старое row[j])
            row[j] = row[j] + row[j - 1]

    return row[-1]

print("Пример 1:", unique_paths(3, 7))  # Выведет: 28
print("Пример 2:", unique_paths(3, 2))  # Выведет: 3