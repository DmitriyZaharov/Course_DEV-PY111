from typing import List
"""
Вода над любой колонкой удерживается только в том случае, если и слева, и справа от неё есть стены выше неё. 
Количество воды над колонкой i ограничено минимумом из самой высокой стены слева и самой высокой стены справа, 
минус высота самой колонки:
Вода = min(max_left, max_right) - height[i]

Используем два указателя (left и right), которые движутся навстречу друг другу.
Сдвигаем тот указатель, у которого текущая стена ниже, так как именно меньшая стена является ограничителем для воды.

Временная сложность: O(N) — мы проходим по массиву ровно один раз.
Пространственная сложность: O(1) — используются только переменные-указатели, дополнительный массив не создается.
"""

def trap(height: List[int]) -> int:
    if not height:
        return 0

    left, right = 0, len(height) - 1
    left_max, right_max = height[left], height[right]
    water = 0

    while left < right:
        # Сдвигаем указатель с меньшей высотой,
        # так как количество воды ограничено меньшей стеной
        if left_max < right_max:
            left += 1
            # Обновляем максимальную высоту слева
            left_max = max(left_max, height[left])
            # Добавляем воду над текущей колонкой
            water += left_max - height[left]
        else:
            right -= 1
            # Обновляем максимальную высоту справа
            right_max = max(right_max, height[right])
            # Добавляем воду над текущей колонкой
            water += right_max - height[right]

    return water

print("Example 1:", trap([0,1,0,2,1,0,1,3,2,1,2,1]))  # Выведет: 6
print("Example 2:", trap([4,2,0,3,2,5])) # Выведет: 9
