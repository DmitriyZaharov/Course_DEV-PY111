def is_subsequence(s: str, t: str) -> bool:
    """
    Эту задачу эффективнее всего решать не через полную матрицу динамического программирования, а
    с помощью классического метода двух указателей. Так как нам нужно проверить сохранение порядка символов,
    мы можем просто по очереди искать символы строки s внутри строки t

    :param s:
    :param t:
    :return:
    """
    """Проверяет, является ли строка s подпоследовательностью строки t."""
    # Указатели для строк s и t соответственно
    p_s = 0
    p_t = 0

    # Двигаемся по обеим строкам, пока не дойдем до конца одной из них
    while p_s < len(s) and p_t < len(t):
        # Если символы совпали, передвигаем указатель в искомой строке s
        if s[p_s] == t[p_t]:
            p_s += 1
        # Указатель в исходной строке t двигаем в любом случае
        p_t += 1

    # Если указатель p_s дошел до конца строки s, значит все символы найдены в нужном порядке
    return p_s == len(s)

"""
    Альтернативный вариант - решение с помощью итераторов:
    char in t_iter ищет символ в итераторе строки t и останавливается на нем. 
    При поиске следующего символа из s сканирование t продолжается строго с того места, где мы остановились, 
    что автоматически сохраняет правильный порядок символов.
"""

def isSubsequence(s: str, t: str) -> bool:
    t_iter = iter(t)
    return all(char in t_iter for char in s)

# Примеры использования:
if __name__ == "__main__":
    # Пример 1 из условия
    print(is_subsequence("abc", "ahbgdc"))  # Выведет: True
    print(isSubsequence("abc", "ahbgdc"))   # Выведет: True

    # Дополнительный пример ("aec" нарушает порядок)
    print(is_subsequence("aec", "abcde"))  # Выведет: False
    print(isSubsequence("aec", "abcde"))  # Выведет: False

    # Пример с пустой строкой (пустая строка всегда является подпоследовательностью)
    print(is_subsequence("", "ahbgdc"))  # Выведет: True
    print(isSubsequence("", "ahbgdc"))  # Выведет: True