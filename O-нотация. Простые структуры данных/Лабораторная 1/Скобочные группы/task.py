"""
Для проверки корректности скобочных групп идеально подходит Стек (Stack),
так как скобки закрываются в порядке LIFO (Last In, First Out) — последняя открытая
скобка должна закрыться первой.
Логика алгоритма:
Если строка пустая, она считается корректной (сразу возвращаем True).
Идем по строке слева направо.Если встречаем открывающую скобку (,
кладем её в стек. Если встречаем закрывающую скобку ), проверяем стек:Если стек пуст — значит,
для закрывающей скобки нет пары, последовательность неверна (False).Если в стеке есть элементы,
убираем одну открывающую скобку из стека (pop).После проверки всей строки стек должен остаться пустым.
Если в нем что-то осталось, значит, какая-то открывающая скобка не закрылась (False).
"""

def check_brackets(brackets_row: str) -> bool:
    """
    Проверьте, является ли входная строка допустимой последовательностью скобок

    :param brackets_row: Входная строка для проверки
    :return: True, если последовательность корректна, False в противном случае
    """
    # Пустая строка — корректная последовательность
    if not brackets_row:
        return True

    # Используем встроенный python-список как стек
    stack = []

    for char in brackets_row:
        if char == '(':
            stack.append(char)  # push
        elif char == ')':
            # Если встретили закрывающую скобку, но стек пуст — пара нарушена
            if not stack:
                return False
            stack.pop()  # pop

    # Если стек пуст, все скобки успешно закрылись
    return len(stack) == 0


if __name__ == '__main__':
    print(check_brackets("()()"))  # True
    print(check_brackets(")("))  # False
    print(check_brackets("(())"))  # True
    print(check_brackets("(()"))  # False
    print(check_brackets(""))  # True
